SUBROUTINE CPG_DDH_TYPE_INIT (SELF, REGISTRY, NLEV, PERSISTENT, YDMODEL, YDDDH)

USE TYPE_MODEL, ONLY : MODEL
USE DDH_MIX, ONLY : TYP_DDH

CLASS (CPG_DDH_TYPE)                             :: SELF
TYPE (FIELD_REGISTRY),            INTENT (IN)    :: REGISTRY
INTEGER (KIND=JPIM),              INTENT (IN)    :: NLEV
LOGICAL, OPTIONAL,                INTENT (IN)    :: PERSISTENT
TYPE (MODEL),                     INTENT (IN)    :: YDMODEL
TYPE (TYP_DDH),                   INTENT (IN)    :: YDDDH


ASSOCIATE (NPROMA => REGISTRY%GEOM%YRDIM%NPROMA, NGPBLKS => REGISTRY%GEOM%YRDIM%NGPBLKS)

CALL FIELD_NEW (SELF%F_AUX3D, UBOUNDS=[NLEV+1, YDMODEL%YRML_DIAG%YRMDDH%NDHIDH, YDDDH%NFIELDS3D_AUTO, NGPBLKS], PERSISTENT=PERSISTENT)
CALL FIELD_NEW (SELF%F_AUX2D, UBOUNDS=[YDMODEL%YRML_DIAG%YRMDDH%NDHIDH, YDDDH%NFIELDS2D_AUTO, NGPBLKS], PERSISTENT=PERSISTENT)

