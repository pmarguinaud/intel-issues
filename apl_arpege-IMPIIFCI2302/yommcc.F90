WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'LMCC03 = ', SELF%LMCC03
WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'LMCC04 = ', SELF%LMCC04
WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'LMCC05 = ', SELF%LMCC05
WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'LMCC06 = ', SELF%LMCC06
